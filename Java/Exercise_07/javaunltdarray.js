function addArrays()
{   
    let arr1 = ["Apple","Orange","Banana","Pears"];
    let arr2 = ["4","3","2","1"]
    let arr3 = ["Favourite fruits"]
    const result = arr3.concat (arr1, arr2);
    return result;
}
 
console.log(addArrays());
/* Concatenation is the process of combining two or more strings to form a new string by subsequently appending the next string to the end of the previous strings.*/